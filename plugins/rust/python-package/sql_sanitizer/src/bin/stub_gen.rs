// Stub file generator for sql_sanitizer module.
// Run with: cargo run --features stub-gen --bin stub_gen

use std::fs;
use std::path::Path;

use sql_sanitizer_rust::stub_info;

const EXTENSION_STUB_PATH: &str = "cpex_sql_sanitizer/sql_sanitizer_rust/__init__.pyi";
const PLUGIN_CORE_CLASS_MARKER: &str = "class SqlSanitizerPluginCore:";
const EXTENSION_STUB_HEADER: &str = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nimport typing\n__all__ = [\n    \"SqlSanitizerPluginCore\",\n]";
const PLUGIN_CORE_CLASS_DEF: &str = "\n\n@typing.final\nclass SqlSanitizerPluginCore:\n    def __new__(cls, config: typing.Any) -> SqlSanitizerPluginCore: ...\n    def prompt_pre_fetch(self, payload: typing.Any, context: typing.Any, extensions: typing.Any = None) -> typing.Any: ...\n    def tool_pre_invoke(self, payload: typing.Any, context: typing.Any, extensions: typing.Any = None) -> typing.Any: ...\n";

fn normalize_stub_content(content: &str) -> String {
    let mut lines = content.lines().map(str::trim_end).collect::<Vec<_>>();
    while matches!(lines.last(), Some(line) if line.is_empty()) {
        lines.pop();
    }
    let mut normalized = lines.join("\n");
    normalized.push('\n');
    normalized
}

fn curate_top_level_stub() {
    let stub_path = Path::new("cpex_sql_sanitizer/__init__.pyi");
    let content = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nfrom .sql_sanitizer import SQLSanitizerPlugin\nfrom .sql_sanitizer_rust import SqlSanitizerPluginCore\n\n__all__ = [\n    \"SqlSanitizerPluginCore\",\n    \"SQLSanitizerPlugin\",\n]\n";
    fs::write(stub_path, normalize_stub_content(content))
        .expect("Failed to write top-level stub file");
    println!("Generated stub at {}", stub_path.display());
}

fn curate_extension_stub() {
    // Ensure the output directory exists
    if let Some(parent) = Path::new(EXTENSION_STUB_PATH).parent() {
        fs::create_dir_all(parent).expect("Failed to create stub directory");
    }
    // `SqlSanitizerPluginCore` is a plain `#[pyclass]` that is not annotated with
    // the pyo3-stub-gen `gen_stub_*` macros, so `generate()` does not emit an
    // extension stub for it (the file may not exist at all).  Start from whatever
    // was generated, if anything, and ensure the hand-written class definition
    // (and a minimal header) is present.
    let existing = fs::read_to_string(EXTENSION_STUB_PATH).unwrap_or_default();
    let content = if existing.contains(PLUGIN_CORE_CLASS_MARKER) {
        existing
    } else if existing.trim().is_empty() {
        format!("{}{}", EXTENSION_STUB_HEADER, PLUGIN_CORE_CLASS_DEF)
    } else {
        format!("{}{}", existing.trim_end(), PLUGIN_CORE_CLASS_DEF)
    };
    let normalized = normalize_stub_content(&content);
    fs::write(EXTENSION_STUB_PATH, &normalized).expect("Failed to write stub file");
    println!("Generated stub at {}", EXTENSION_STUB_PATH);
}

fn main() {
    let stub_info = stub_info().expect("Failed to get stub info");
    stub_info.generate().expect("Failed to generate stub files");
    curate_extension_stub();
    curate_top_level_stub();
    println!("\u{2713} Generated stub files successfully");
}
