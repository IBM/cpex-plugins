// Generates Python type stubs (.pyi) for the secrets_detection_rust module.

use std::fs;
use std::path::Path;

use secrets_detection_rust::stub_info;

fn curate_extension_stub() {
    let stub_path = Path::new("cpex_secrets_detection/secrets_detection_rust/__init__.pyi");
    let mut content = fs::read_to_string(stub_path).expect("Failed to read generated stub file");
    content = content.replace(
        "\"py_scan_container\",\n]",
        "\"SecretsDetectionPluginCore\",\n    \"py_scan_container\",\n]",
    );
    if !content.contains("class SecretsDetectionPluginCore:") {
        content.push_str(
            "\n\n@typing.final\nclass SecretsDetectionPluginCore:\n    def __new__(cls, config: dict) -> SecretsDetectionPluginCore: ...\n    def prompt_pre_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_post_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def resource_post_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n",
        );
    }
    fs::write(stub_path, content).expect("Failed to write curated stub file");
}

fn curate_top_level_stub() {
    let stub_path = Path::new("cpex_secrets_detection/__init__.pyi");
    let content = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nfrom .secrets_detection import SecretsDetectionPlugin\nfrom .secrets_detection_rust import py_scan_container\n\n__all__ = [\n    \"SecretsDetectionPlugin\",\n    \"py_scan_container\",\n]\n";
    fs::write(stub_path, content).expect("Failed to write curated top-level stub file");
}

fn main() {
    let stub_info = stub_info().expect("Failed to get stub info");
    stub_info.generate().expect("Failed to generate stub file");
    curate_top_level_stub();
    curate_extension_stub();
    println!("✓ Generated stub files successfully");
}
