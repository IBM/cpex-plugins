// Stub file generator for pii_filter module
//
// This binary generates Python type stub files (.pyi) for the pii_filter module.
// Run with: cargo run --bin stub_gen

use std::fs;
use std::path::Path;

use pii_filter_rust::stub_info;

fn curate_extension_stub() {
    let stub_path = Path::new("cpex_pii_filter/pii_filter_rust/__init__.pyi");
    let mut content = fs::read_to_string(stub_path).expect("Failed to read generated stub file");
    content = content.replace(
        "\"PIIDetectorRust\",\n]",
        "\"PIIDetectorRust\",\n    \"PIIFilterPluginCore\",\n]",
    );
    if !content.contains("class PIIFilterPluginCore:") {
        content.push_str(
            "\n\n@typing.final\nclass PIIFilterPluginCore:\n    def __new__(cls, config: typing.Any) -> PIIFilterPluginCore: ...\n    def prompt_pre_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def prompt_post_fetch(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_pre_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n    def tool_post_invoke(self, payload: typing.Any, context: typing.Any) -> typing.Any: ...\n",
        );
    }
    fs::write(stub_path, content).expect("Failed to write curated stub file");
}

fn curate_top_level_stub() {
    let stub_path = Path::new("cpex_pii_filter/__init__.pyi");
    let content = "# This file is automatically generated by pyo3_stub_gen\n# ruff: noqa: E501, F401, F403, F405\n\nfrom .pii_filter import PIIFilterPlugin\nfrom .pii_filter_rust import PIIDetectorRust\n\n__all__ = [\n    \"PIIDetectorRust\",\n    \"PIIFilterPlugin\",\n]\n";
    fs::write(stub_path, content).expect("Failed to write curated top-level stub file");
}

fn main() {
    // Get stub info (returns Result)
    let stub_info = stub_info().expect("Failed to get stub info");

    // Generate stub files - paths are determined from pyproject.toml
    stub_info.generate().expect("Failed to generate stub file");
    curate_top_level_stub();
    curate_extension_stub();

    println!("✓ Generated stub files successfully");
}
